# ---------- Arithmatic operator----------
print("Arithmatic operator")
print("5+6 is",5+6)
print("5-6 is",5-6)
print("5*6 is",5*6)
print("5/6 is",5/6)
print("5**6 is",5**6)
print("5%5 is",5%5)
print("15//6 is",15//6)



# ----------Assignment operator(/,*,-,+,%---) ----------
print("Assignment operator")
x=5
print(x)
x+=1
print(x)



# ----------Comparision operator----------
print("Comparision operator")
i=5
print(i==8)
print(i==5)
print(i<=9)
print(i>3)
print(i!=7)



# ----------Logical operator----------
print("Logical operator")
a=True
b=False
print(a or b)
print(a and b)



# ----------Identity operator--------
print("Identity operator")
a=True
b=False
print(a is b)
print( a is not b)
print(5 is not 7)



# ----------Membership operator----------
print("Membership operator")
list =[10,64,0,54,0,6,4,6,5995,9,8]
print(9 in list)
print(9 not in list)



# ----------Bitwise operator----------
"""
DECIMAL TO BINARY
0-->00
1-->01
2-->10
3-->11
"""
print("Bitwise operator")
print(0 & 1)
print(0|1)
print(0|3)
print(1&2)
